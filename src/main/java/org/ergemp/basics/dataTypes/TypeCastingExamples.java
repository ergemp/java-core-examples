package org.ergemp.basics.dataTypes;

public class TypeCastingExamples {
    public static void main(String[] args) {

        // ask Java to treat a variable of one type like it is another one

        int iNumber1 = 5;
        int iNumber2 = 6;

        float fNumber = (float)iNumber1/iNumber2;

        System.out.println(fNumber);



    }
}
